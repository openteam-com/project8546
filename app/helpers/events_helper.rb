module EventsHelper
  def related(event)
    {}.tap{ |hash|
      event.related_items.order('id desc').each do |item|
        hash["#{item.item_type}_#{item.item_id}"] = JSON.parse($redis.get "#{item.item_type}_#{item.item_id}")
      end
    }
  end

  def event_id
    params[:event_id] || 1
  end
end
